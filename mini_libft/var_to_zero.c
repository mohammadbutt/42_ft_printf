#include "mini_libft.h"

/*
** sets variables to zero
*/
int var_to_zero(int_fast64_t *a, int *b, int *c, int *d)
{
	*a = 0;
	*b = 0;
	*c = 0;
	*d = 0;
	return(0);
}
